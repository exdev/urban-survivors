using UnityEngine;
using System.Collections;
using System.Collections.Generic;

///////////////////////////////////////////////////////////////////////////////
// defines
///////////////////////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////////////////////
// class MissionLevels
// 
// Purpose: 
// 
///////////////////////////////////////////////////////////////////////////////

public class MissionLevels : MonoBehaviour {

    int currentLevel = 0;

    ///////////////////////////////////////////////////////////////////////////////
    //
    ///////////////////////////////////////////////////////////////////////////////

    public GameObject[] levelup_behaviors = new GameObject[0];


    ///////////////////////////////////////////////////////////////////////////////
    //
    ///////////////////////////////////////////////////////////////////////////////

    void Awake () {
    }

    // ------------------------------------------------------------------ 
    // Desc: 
    // ------------------------------------------------------------------ 

    void OnMissionLevelUp () {
        if ( currentLevel < levelup_behaviors.Length ) {
            levelup_behaviors[currentLevel].SendMessage("OnLevelUp");
            ++currentLevel;
        }
        else {
            Debug.LogError("TODO: finished!!!!");
        }
    }
}
