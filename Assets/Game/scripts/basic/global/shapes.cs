using UnityEngine;
using System.Collections;

///////////////////////////////////////////////////////////////////////////////
// defines
///////////////////////////////////////////////////////////////////////////////

// ------------------------------------------------------------------ 
// Desc: Circle
// ------------------------------------------------------------------ 

[System.Serializable]
public class Circle {
    public Vector2 center;
    public float radius;

    public bool Contains ( Vector2 _pos ) {
        return ((_pos - center).magnitude <= radius);
    }
}

